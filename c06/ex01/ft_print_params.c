#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	*name;

	i = 0;
	while (++i < argc)
	{
		name = argv[i];
		while (*name != '\0')
			write(1, name++, 1);
		write(1, "\n", 1);
	}
}
